package org.joycore.rpg.object;

public class StatsObject extends ActiveObject {

	private Integer strength;
	
	private Integer agility;

	private Integer stamina;
	
	private Integer intellect;
	
	private Integer spirit;
	
	private Integer mastery;
	
	private Integer haste;
	
	private Integer hit;
	
	private Integer crit;
	
	private Integer speed;

	public Integer getStrength() {
		return strength;
	}

	public void setStrength(Integer strength) {
		this.strength = strength;
	}

	public Integer getAgility() {
		return agility;
	}

	public void setAgility(Integer agility) {
		this.agility = agility;
	}

	public Integer getStamina() {
		return stamina;
	}

	public void setStamina(Integer stamina) {
		this.stamina = stamina;
	}

	public Integer getIntellect() {
		return intellect;
	}

	public void setIntellect(Integer intellect) {
		this.intellect = intellect;
	}

	public Integer getSpirit() {
		return spirit;
	}

	public void setSpirit(Integer spirit) {
		this.spirit = spirit;
	}

	public Integer getMastery() {
		return mastery;
	}

	public void setMastery(Integer mastery) {
		this.mastery = mastery;
	}

	public Integer getHaste() {
		return haste;
	}

	public void setHaste(Integer haste) {
		this.haste = haste;
	}

	public Integer getHit() {
		return hit;
	}

	public void setHit(Integer hit) {
		this.hit = hit;
	}

	public Integer getCrit() {
		return crit;
	}

	public void setCrit(Integer crit) {
		this.crit = crit;
	}

	public Integer getSpeed() {
		return speed;
	}

	public void setSpeed(Integer speed) {
		this.speed = speed;
	}
}
